package com.diopujaandika.kotlin.fundamental

/*
    Program ini mencetak langkah-langkah memasak sebuah hidangan ke layar.

    Setiap resep dibagi menjadi tiga bagian utama:
    1. Persiapan Awal
    2. Proses Memasak
    3. Penyajian Akhir

    Jika ingin menambahkan proses baru, pastikan langkah tersebut ditempatkan
    di bagian yang sesuai agar urutan memasak tetap logis dan mudah dipahami.
*/
fun main() {
    println("Tutorial Memasak Mie Instan")

    // Persiapan awal
    println("1. Siapkan panci dan air secukupnya.")
    println("2. Nyalakan kompor dan panaskan air.")

    // Proses memasak
    println("3. Setelah air mendidih, masukkan mie ke dalam panci.")
    println("4. Masak mie selama 3 menit sambil diaduk perlahan.")
    println("5. Sambil menunggu, siapkan bumbu mie di mangkuk.")

    // Penyajian akhir
    println("6. Setelah matang, tiriskan mie jika perlu.")
    println("7. Campurkan mie dengan bumbu dan aduk hingga merata.")
    println("8. Mie instan siap disajikan!")
}

