package com.diopujaandika.kotlin.tipe_data

fun main()  {
    // Var => Dapat senantiasa memperbarui nilainya.
    var companyName: String = "Dicoding"
    companyName = "Dicoding Academy"
    println(companyName)

    // Val => Tidak diperbolehkan untuk mengubah nilai inisialisasinya.
    val namaPerusahan: String = "Dicoding"
    println(namaPerusahan)
}