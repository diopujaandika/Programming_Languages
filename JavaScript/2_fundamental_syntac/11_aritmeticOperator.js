/**
 * ARITHMETIC OPERATOR
 * Arithmetic Operator adalah operator yang digunakan untuk proses aritmatika.
 */

let angka1 = 10
let angka2 = 2

    //Penjumlahan
    console.log(angka1+angka2)

    //Pengurangan
    console.log(angka1-angka2)

    //Perkalian
    console.log(angka1*angka2)

    //Pembagian
    console.log(angka1/angka2)

    //Modulus/Sisa Bagi [%]
    console.log(angka1 % angka2)

    //Increment/Penjumlahan satu angka[++]
    ++angka1
    console.log(angka1)
    angka2++
    console.log(angka2)
    
    //Derement/Pengurangan satu angka [--]
    --angka1
    console.log(angka1)
    angka2--
    console.log(angka2)

    //Eksponensial/Pangkat [**]
    let angka3 = angka1**2
    console.log(angka3)
