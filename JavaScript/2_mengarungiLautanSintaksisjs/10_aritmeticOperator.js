/*
 * OPERATOR 
 * Operator adalah simbol atau teks yang digunakan untuk melakukan sebuah operasi.
 * OPERAN
 * Operan adalah nilai yang menjadi target dari sebuah operasi
 */

//Aritmetic Operator
let angka1 = 10
let angka2 = 2

    //Modulus/Sisa Bagi [%]
    console.log(angka1 % angka2)

    //Increment/Penjumlahan satu angka[++]
    ++angka1
    console.log(angka1)
    angka2++
    console.log(angka2)
    
    //Derement/Pengurangan satu angka [--]
    --angka1
    console.log(angka1)
    angka2--
    console.log(angka2)

    //Eksponensial/Pangkat [**]
    let angka3 = angka1**2
    console.log(angka3)
