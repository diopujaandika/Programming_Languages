/*
    PARADIGMA OOP
    OOP itu terdiri dari
    1. Attributes/Properties
    2. Method/Behavior

    Teradapat 4 pilar dalam pemorgraman berorientasi object:
    1. Encapsulation 
      - Tempat menyimpan sesuatu.
      - berfungsi sengumpulkan data dalam metode yang berhubungan ke dalam sebuah objek.
      - Data dapat diisolasi dan tidak dapat diakses langsung dari luar (Objek lain tidak bisa mengubah value dari property secara langsung)
    2. Abstraction
      - Objek hanya menunjukan operasi secara high-level
    3. Inheritance
      - Memungkinkan objek bisa mewarisi atribut dan method yang sama, namun bukan berasal dari objek yang sama.
      - Objek turunan (subclass) mewarisi semua atribut dan method dari objek induknya (superclass)
    4. Polymorphism
      - Objek memiliki banyak bentuk atau implementasi yang berbeda-beda pada suatu metode yang sama.
 */